char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		int	temp;

		temp = src[i];
		src[i] = dest[i];
		dest[i] = temp;
		i++;
	}
}
